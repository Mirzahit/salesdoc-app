-- Модули → уроки (конспект-карточки + роль тренажёра + вопросы теста) → прогресс по email.
-- RLS не используем: доступ только через /api/academy (x-app-token + x-user-email, как везде).
-- Правильные ответы (questions[].correct) наружу не отдаются — тест проверяет сервер.

CREATE TABLE IF NOT EXISTS academy_modules (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  sort INT NOT NULL DEFAULT 100,
  title TEXT NOT NULL,
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS academy_lessons (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  module_id UUID NOT NULL REFERENCES academy_modules(id) ON DELETE CASCADE,
  sort INT NOT NULL DEFAULT 100,
  title TEXT NOT NULL,
  duration_label TEXT,
  cards JSONB NOT NULL DEFAULT '[]',      -- [{title, text}]
  trainer JSONB,                          -- {role, goal, criteria}; NULL = урок без тренажёра
  questions JSONB NOT NULL DEFAULT '[]',  -- [{q, options[4], correct}]
  active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);
CREATE INDEX IF NOT EXISTS idx_academy_lessons_module ON academy_lessons(module_id, sort);

CREATE TABLE IF NOT EXISTS academy_progress (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_email TEXT NOT NULL,
  lesson_id UUID NOT NULL REFERENCES academy_lessons(id) ON DELETE CASCADE,
  notes_done BOOLEAN NOT NULL DEFAULT FALSE,
  trainer_score INT,
  trainer_review TEXT,
  test_score INT,
  test_attempts INT NOT NULL DEFAULT 0,
  passed BOOLEAN NOT NULL DEFAULT FALSE,
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE(user_email, lesson_id)
);
CREATE INDEX IF NOT EXISTS idx_academy_progress_email ON academy_progress(user_email);

